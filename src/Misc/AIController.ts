    private CalcProgressZone(kitty: Kitty) {
        let currentProgressZoneId = kitty.ProgressZone

        if (this.IsInSafeZone(kitty.Unit.x, kitty.Unit.y, currentProgressZoneId + 1)) {
            currentProgressZoneId++
        }

        return currentProgressZoneId
    }

