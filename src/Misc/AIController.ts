    AngleOf(point: [number, number], center: [number, number]) {
        const deltaX: number = point[0] - center[0]
        const deltaY: number = point[1] - center[1]
        const radians: number = Math.atan2(deltaY, deltaX)
        return this.NormalizeAngle(radians)
    }

